#Thats it for basic math, now for logic! The most simple logic is the if statement, which will happen IF something is true


number = 5

if number == 5:		#The if statement (== just sees if the two are equal), notice the colon
	print("your number is 5") 	#Notice how this code is indented
else:	#This will happen if the if is false
	print("your number is not 5")	#again, notice the indentation

